    if (size) parts.push(`Розмір: ${size}`);
    if (duration) parts.push(`Тривалість: ${duration}`);
    if (params) parts.push(`Параметри: ${params}`);
    if (desc) parts.push(`Опис: ${desc}`);
    if (effect) parts.push(`ЕФЕКТ: ${effect}`);
